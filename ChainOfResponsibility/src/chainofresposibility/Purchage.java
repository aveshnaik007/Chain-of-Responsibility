package chainofresposibility;

public class Purchage {
	Integer purchageNum;
	double amout;
	
	public Integer getPurchageNum() {
		return purchageNum;
	}
	public void setPurchageNum(Integer purchageNum) {
		this.purchageNum = purchageNum;
	}
	public double getAmout() {
		return amout;
	}
	public void setAmout(double amout) {
		this.amout = amout;
	}
	
}
